#include <stdio.h>

/**
 * main - Printing alphabets in both small and large cases
 * Return: Always 0 (Success)
 */
int main(void)
{
	char k;

	char m;

	k = 'a';
	m = 'A';
	while 
		(k <= 'z') {
			putchar(k);
			k++;
		}
	while 
		(m <= 'Z') {
			putchar(m);
			m++;
		}
	putchar('\n');
	return (0);

}
