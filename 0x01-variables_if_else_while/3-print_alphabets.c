#include <stdio.h>

/**
 * main - Printing alphabets in both small and large cases
 * Return: Always 0 (Success)
 */
int main(void)
{
	int alp;

	for 
		(alp = 'a'; alp <= 'z'; alp++);
	                putchar(alp);
	for 
		(alp = 'A'; alp <= 'Z'; alp++);
                	putchar(alp);
	putchar('\n');
	return (0);

}
